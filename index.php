                <p>John Richard Deacon (born 19 August 1951) is an English retired musician, best known for being the bassist for the rock band Queen. He composed several songs for the group, including Top 10 hits "You're My Best Friend", "Another One Bites the Dust", and "I Want to Break Free"; co-wrote "Under Pressure", "Friends Will Be Friends" and "One Vision"; and he was involved in the band's financial management.

Deacon grew up in Oadby, Leicestershire, playing bass in a local band, The Opposition, before moving to study electronics at Chelsea College, London. He joined Queen in 1971 on the strength of his musical and electronic skills, particularly the home-made Deacy Amp which guitarist Brian May used to create guitar orchestras throughout Queen's career. From the third album, Sheer Heart Attack, onwards, he wrote at least one song per album, several of which became hits. As well as bass, Deacon played some guitar, keyboards and piano on Queen's studio work.

After the death of lead singer Freddie Mercury in 1991 and the following year's Tribute Concert, Deacon performed only sporadically with the remaining members of Queen before retiring from the music industry in 1997 after recording "No-One but You (Only the Good Die Young)". He has not performed on any of the other projects that the two remaining members, Brian May and Roger Taylor, have put together. He was inducted into the Rock and Roll Hall of Fame as a member of Queen in 2001 and into Songwriters Hall of Fame in 2003.</p>
